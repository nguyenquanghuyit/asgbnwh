		// Load recordset
		if ($selectLimit) {
			$this->TotalRecs = $this->listRecordCount();
		} else {
			if (!$this->Recordset)
				$this->Recordset = $this->loadRecordset();
			$rs = &$this->Recordset;
			if ($rs)
				$this->TotalRecs = $rs->RecordCount();
		}
		$this->StartRec = 1;
		$this->setupStartRec(); // Set up start record position

		// Set the last record to display
		if ($this->DisplayRecs <= 0) {
			$this->StopRec = $this->TotalRecs;
		} else {
			$this->StopRec = $this->StartRec + $this->DisplayRecs - 1;
		}
		$this->ExportDoc = GetExportDocument($this, "v");
		$doc = &$this->ExportDoc;
		if (!$doc)
			$this->setFailureMessage($Language->phrase("ExportClassNotFound")); // Export class not found
		if (!$rs || !$doc) {
			RemoveHeader("Content-Type"); // Remove header
			RemoveHeader("Content-Disposition");
			$this->showMessage();
			return;
		}
		if ($selectLimit) {
			$this->StartRec = 1;
			$this->StopRec = $this->DisplayRecs <= 0 ? $this->TotalRecs : $this->DisplayRecs;
		}

		// Call Page Exporting server event
		$this->ExportDoc->ExportCustom = !$this->Page_Exporting();
		$header = $this->PageHeader;
		$this->Page_DataRendering($header);
		$doc->Text .= $header;
		$this->exportDocument($doc, $rs, $this->StartRec, $this->StopRec, "view");

		// Export detail records (tbl_goodsinpack)
		if (EXPORT_DETAIL_RECORDS && in_array("tbl_goodsinpack", explode(",", $this->getCurrentDetailTable()))) {
			global $tbl_goodsinpack;
			if (!isset($tbl_goodsinpack))
				$tbl_goodsinpack = new tbl_goodsinpack();
			$rsdetail = $tbl_goodsinpack->loadRs($tbl_goodsinpack->getDetailFilter()); // Load detail records
			if ($rsdetail && !$rsdetail->EOF) {
				$exportStyle = $doc->Style;
				$doc->setStyle("h"); // Change to horizontal
				if (!$this->isExport("csv") || EXPORT_DETAIL_RECORDS_FOR_CSV) {
					$doc->exportEmptyRow();
					$detailcnt = $rsdetail->RecordCount();
					$oldtbl = $doc->Table;
					$doc->Table = $tbl_goodsinpack;
					$tbl_goodsinpack->exportDocument($doc, $rsdetail, 1, $detailcnt);
					$doc->Table = $oldtbl;
				}
				$doc->setStyle($exportStyle); // Restore
				$rsdetail->close();
			}
		}
		$footer = $this->PageFooter;
		$this->Page_DataRendered($footer);
		$doc->Text .= $footer;

		// Close recordset
		$rs->close();

		// Call Page Exported server event
		$this->Page_Exported();

		// Export header and footer
		$doc->exportHeaderAndFooter();

		// Clean output buffer (without destroying output buffer)
		$buffer = ob_get_contents(); // Save the output buffer
		if (!DEBUG_ENABLED && $buffer)
			ob_clean();

		// Write debug message if enabled
		if (DEBUG_ENABLED && !$this->isExport("pdf"))
			echo GetDebugMessage();

		// Output data
		if ($this->isExport("email")) {

			// Export-to-email disabled
		} else {
			$doc->export();
			if ($return) {
				RemoveHeader("Content-Type"); // Remove header
				RemoveHeader("Content-Disposition");
				$content = ob_get_contents();
				if ($content)
					ob_clean();
				if ($buffer)
					echo $buffer; // Resume the output buffer
				return $content;
			}
		}
	}

	// Set up master/detail based on QueryString
	protected function setupMasterParms()
	{
		$validMaster = FALSE;

		// Get the keys for master table
		if (Get(TABLE_SHOW_MASTER) !== NULL) {
			$masterTblVar = Get(TABLE_SHOW_MASTER);
			if ($masterTblVar == "") {
				$validMaster = TRUE;
				$this->DbMasterFilter = "";
				$this->DbDetailFilter = "";
			}
			if ($masterTblVar == "tbl_packing") {
				$validMaster = TRUE;
				if (Get("fk_Id_packing") !== NULL) {
					$this->ID_packing->setQueryStringValue(Get("fk_Id_packing"));
					$this->ID_packing->setSessionValue($this->ID_packing->QueryStringValue);
					if (!is_numeric($this->ID_packing->QueryStringValue))
						$validMaster = FALSE;
				} else {
					$validMaster = FALSE;
				}
			}
		} elseif (Post(TABLE_SHOW_MASTER) !== NULL) {
			$masterTblVar = Post(TABLE_SHOW_MASTER);
			if ($masterTblVar == "") {
				$validMaster = TRUE;
				$this->DbMasterFilter = "";
				$this->DbDetailFilter = "";
			}
			if ($masterTblVar == "tbl_packing") {
				$validMaster = TRUE;
				if (Post("fk_Id_packing") !== NULL) {
					$this->ID_packing->setFormValue(Post("fk_Id_packing"));
					$this->ID_packing->setSessionValue($this->ID_packing->FormValue);
					if (!is_numeric($this->ID_packing->FormValue))
						$validMaster = FALSE;
				} else {
					$validMaster = FALSE;
				}
			}
		}
		if ($validMaster) {

			// Save current master table
			$this->setCurrentMasterTable($masterTblVar);
			$this->setSessionWhere($this->getDetailFilter());

			// Reset start record counter (new master key)
			if (!$this->isAddOrEdit()) {
				$this->StartRec = 1;
				$this->setStartRecordNumber($this->StartRec);
			}

			// Clear previous master key from Session
			if ($masterTblVar <> "tbl_packing") {
				if ($this->ID_packing->CurrentValue == "")
					$this->ID_packing->setSessionValue("");
			}
		}
		$this->DbMasterFilter = $this->getMasterFilter(); // Get master filter
		$this->DbDetailFilter = $this->getDetailFilter(); // Get detail filter
	}

	// Set up detail parms based on QueryString
	protected function setupDetailParms()
	{

		// Get the keys for master table
		if (Get(TABLE_SHOW_DETAIL) !== NULL) {
			$detailTblVar = Get(TABLE_SHOW_DETAIL);
			$this->setCurrentDetailTable($detailTblVar);
		} else {
			$detailTblVar = $this->getCurrentDetailTable();
		}
		if ($detailTblVar <> "") {
			$detailTblVar = explode(",", $detailTblVar);
			if (in_array("tbl_goodsinpack", $detailTblVar)) {
				if (!isset($GLOBALS["tbl_goodsinpack_grid"]))
					$GLOBALS["tbl_goodsinpack_grid"] = new tbl_goodsinpack_grid();
				if ($GLOBALS["tbl_goodsinpack_grid"]->DetailView) {
					$GLOBALS["tbl_goodsinpack_grid"]->CurrentMode = "view";

					// Save current master table to detail table
					$GLOBALS["tbl_goodsinpack_grid"]->setCurrentMasterTable($this->TableVar);
					$GLOBALS["tbl_goodsinpack_grid"]->setStartRecordNumber(1);
					$GLOBALS["tbl_goodsinpack_grid"]->ID_packingDetail->IsDetailKey = TRUE;
					$GLOBALS["tbl_goodsinpack_grid"]->ID_packingDetail->CurrentValue = $this->ID_packingDetail->CurrentValue;
					$GLOBALS["tbl_goodsinpack_grid"]->ID_packingDetail->setSessionValue($GLOBALS["tbl_goodsinpack_grid"]->ID_packingDetail->CurrentValue);
				}
			}
		}
	}

